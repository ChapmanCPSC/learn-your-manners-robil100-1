class EmailViewController: UIViewController {

    @IBOutlet weak var emailTextField: UITextField!
    @IBOutlet weak var errorLabel: UILabel!
    
    let emailPref = NSUserDefaults.standardUserDefaults()
    
    override func viewDidLoad() {
